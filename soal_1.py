print("Program Operasi Aritmatika")
nilai_a = float(input("Masukkan nilai A: "))
nilai_b = float(input("Masukkan nilai B: "))
hasil_tambah = nilai_a + nilai_b
hasil_kurang = nilai_a - nilai_b
hasil_kali = nilai_a * nilai_b
print("\nHasil Operasi:")
print(f"A + B = {hasil_tambah}")
print(f"A - B = {hasil_kurang}")
print(f"A * B = {hasil_kali}")

print("\nProgram selesai.")